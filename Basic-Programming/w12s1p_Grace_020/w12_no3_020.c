#include <stdio.h>

int main(){
	
	int x =5;	
	x=5;	
	x++;
	printf("x++ = %d",x);
	
	x=5;
	x--;
	printf("\nx-- = %d",x);
	
	x=5;
	++x;
	printf("\n++x = %d",x);
	
	x=5;
	--x;
	printf("\n--x = %d",x);	
	return 1;
}

